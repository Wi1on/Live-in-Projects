function next1() {
  const hasilImage = document.querySelector(".scroll1");
  const direction = 1.1;
  const scrollAmount = hasilImage.clientWidth * direction;
  hasilImage.scrollBy({ left: scrollAmount, behavior: "smooth"});
}

